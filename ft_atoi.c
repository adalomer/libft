#include "libft.h"

int	ft_atoi(const char *str)
{
	int	b;
	int	c;
	int	d;

	b = 0;
	c = 1;
	d = 0;
	while ((str[b] >= 9 && str[b] <= 13) || str[b] == 32)
		b++;
	if (str[b] == '-')
	{
		c = -1;
		b++;
	}
	else if (str[b] == '+')
		b++;
	while (str[b] >= '0' && str[b] <= '9')
	{
		d = d * 10 + (str[b] - '0');
		b++;
	}
	return (d * c);
}
