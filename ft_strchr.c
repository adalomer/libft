char *ft_strchr(const char *a, int c)
{
    int b;
    char *d;
    d = (char *)a;
    b = 0;
    while(d[b] != '\0')
    {
        if (d[b] == c)
        return(&d[b]);
        b++;
    }
    return (0);
}